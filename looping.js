// contoh penggunaan while
let i = 0;
while (i < 10) {
  console.log(`perulangan ke-${i}`);
  i++;
}

// contoh penggunaan for
for (let i = 0; i < 10; i++) {
  console.log(`perulangan ke-${i}`);
}

// contoh penggunaan do while
let j = 0;
do {
  console.log(`perulangan ke-${j}`);
  j++;
} while (j < 10);
